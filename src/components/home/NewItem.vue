                            0: {
                                items: nb_column3,
                                nav: total_product <= nb_column3 ? false : ((true) ? true : false),
                            },
                            767: {
                                items: nb_column2,
                                nav: total_product <= nb_column2 ? false : ((true) ? true : false),
                            },
                            991: {
                                items: nb_column1,
                                nav: total_product <= nb_column1 ? false : ((true) ? true : false),
                            },
